#include <iostream>
using namespace std;

int main() {
 
    cout << "Perulangan"<<endl;
    for (int i = 1; i <= 10; ++i) {
        cout << i <<endl;
    }
    cout << endl;
    
    cout << "Perulangan Menurun"<<endl;
    for (int i = 10; i >= 1; --i) {
        cout << i <<endl;
    }
    cout << endl;

    return 0;
}

